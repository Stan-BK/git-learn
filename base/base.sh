# 解开注释后运行以查看效果（命令行中直接运行该文件路径即可）

# 保存更改到暂存区
# git add .

# 提交更改到本地仓库，""为提交备注内容
# git commit -m ""

# 提交更改到远程仓库
# git push
# 可更改提交者等配置信息，git config以查看更多可配置项
# git config

# 查看历史提交版本信息，按q键退出查看
# git log
# 查看所有历史操作信息
# git reflog

# 回退到相应ID版本
# git reset --hard ${ID}

# 撤销更改
# 撤销本地修改
# git restore ${filePath}
# 撤销暂存区修改
# git restore --staged ${filePath}

# 连接远程仓库
# git remote ${repository}
# 获取远程仓库
# git fetch ${repository}
# 拉取远程仓库并合并(git fetch + git merge)
# git pull

